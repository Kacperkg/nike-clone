class Command(BaseCommand):
    help = "Seed the database with Nike products for development"

    def add_arguments(self, parser):
        parser.add_argument(
            "--clear",
            action="store_true",
            help="Delete all existing products before seeding",
        )

    def handle(self, *args, **options):
        if options["clear"]:
            count = Product.objects.count()
            Product.objects.all().delete()
            self.stdout.write(self.style.WARNING(f"Deleted {count} existing products"))

        created = 0
        for data in PRODUCTS:
            extra_images = data.pop("extra_images", [])
            product, was_created = Product.objects.get_or_create(
                name=data["name"],
                subtitle=data["subtitle"],
                category=data["category"],
                defaults=data,
            )
            if was_created:
                created += 1
                for img_url in extra_images:
                    ProductImage.objects.create(
                        product=product,
                        image=img_url,
                        alt_text=f"{product.name} image",
                    )

        self.stdout.write(
            self.style.SUCCESS(
                f"Seeded {created} new products (total: {Product.objects.count()})"
            )
        )
